/* ============ BINDINGS ============ */
function bind(t){
  // scroll links
  document.querySelectorAll("[data-scroll]").forEach(el=>{
    el.addEventListener("click",()=>{
      const id=el.getAttribute("data-scroll");
      const target=id==="top"?document.body:document.getElementById(id);
      target?.scrollIntoView({behavior:"smooth"});
      $("#mobileMenu")?.classList.remove("open");
    });
  });
  // theme
  $("#themeBtn")?.addEventListener("click",()=>{
    THEME=THEME==="dark"?"light":"dark";
    document.documentElement.setAttribute("data-theme",THEME);
    $("#themeBtn").innerHTML=THEME==="light"?moonIcon:sunIcon;
  });
  // lang
  document.querySelectorAll("[data-lang]").forEach(b=>{
    b.addEventListener("click",()=>{ LANG=b.getAttribute("data-lang"); CAPS=[]; GER=0; render(); });
  });
  // burger
  $("#burgerBtn")?.addEventListener("click",()=>$("#mobileMenu")?.classList.toggle("open"));
  // tema textarea
  const tema=$("#tema"), genBtn=$("#genBtn"), cc=$("#cc");
  tema?.addEventListener("input",()=>{
    cc.textContent=tema.value.length;
    const has=!!tema.value.trim();
    genBtn.disabled=!has;
    genBtn.style.opacity=has?"1":".5";
    genBtn.style.cursor=has?"pointer":"not-allowed";
    genBtn.innerHTML=(has?t.genBtn:t.genBtnEmpty)+" ✨";
  });
  // tons
  document.querySelectorAll("[data-tone]").forEach(b=>{
    b.addEventListener("click",()=>{
      TOM=b.getAttribute("data-tone");
      document.querySelectorAll("[data-tone]").forEach(x=>x.classList.toggle("on",x===b));
      if(CAPS.length) doGen(t,false);
    });
  });
  // upload
  const upload=$("#upload"), file=$("#file");
  upload?.addEventListener("click",()=>file.click());
  file?.addEventListener("change",e=>{
    const f=e.target.files&&e.target.files[0]; if(!f)return;
    const r=new FileReader();
    r.onload=ev=>{ FOTO=ev.target.result; $("#upInner").innerHTML=`<img src="${FOTO}" style="width:96px;height:96px;object-fit:cover;border-radius:12px">`; upload.style.flex="0 0 96px"; };
    r.readAsDataURL(f);
  });
  // gerar
  genBtn?.addEventListener("click",()=>{ GER=0; doGen(t,true); });
  // faq
  document.querySelectorAll("[data-faq]").forEach(el=>{
    el.addEventListener("click",()=>el.classList.toggle("open"));
  });
}

function doGen(t,reset){
  const tema=$("#tema").value;
  if(!tema.trim()){ CAPS=[]; return; }
  if(reset) GER=0;
  CAPS=[0,1,2,3].map(i=>generate(LANG,tema,TOM,i+GER*4));
  renderResults(t,tema);
}

function renderResults(t,tema){
  const tags=makeTags(LANG,tema,TOM);
  const box=$("#results");
  let html=`<div class="res-head"><span>${CAPS.length} ${t.suggestions}</span><button class="reroll" id="reroll">↻ ${t.more}</button></div>`;
  CAPS.forEach((l,i)=>{
    const full=l+(tags.length?"\n\n"+tags.join(" "):"");
    html+=`<div class="cap"><p>${esc(l)}</p><div class="cap-tags">${tags.join(" ")}</div>
      <div class="cap-act"><button class="copy" data-copy="${i}">${t.copy}</button><span class="cap-len">${full.length} ${t.chars}</span></div></div>`;
  });
  box.innerHTML=html;
  $("#reroll")?.addEventListener("click",()=>{ GER++; doGen(t,false); });
  box.querySelectorAll("[data-copy]").forEach(b=>{
    b.addEventListener("click",()=>{
      const i=+b.getAttribute("data-copy");
      const full=CAPS[i]+(tags.length?"\n\n"+tags.join(" "):"");
      navigator.clipboard&&navigator.clipboard.writeText(full);
      b.textContent="✓ "+t.copied; setTimeout(()=>b.textContent=t.copy,1500);
    });
  });
}

/* reveal on scroll */
function observeReveals(){
  const io=new IntersectionObserver((es)=>{es.forEach(e=>{if(e.isIntersecting){e.target.classList.add("in");io.unobserve(e.target);}});},{threshold:.12});
  document.querySelectorAll(".reveal").forEach(el=>io.observe(el));
}

