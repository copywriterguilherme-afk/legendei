function cap(s){ return s ? s.charAt(0).toUpperCase() + s.slice(1) : s; }
function pick(a, s){ return a[Math.abs(s) % a.length]; }

function generate(lang, tema, tom, idx) {
  const t = (tema || "").trim() || "seu post";
  const s = idx + 1;
  const eng = ENGINE[lang] || ENGINE.pt;
  const e = (k) => pick(EMOJIS[tom] || EMOJIS.conteudo, s + k);
  if (tom === "autor") {
    const q = pick(eng.autores, s);
    const opts = [`"${q.f}"\n— ${q.a} ${e(0)}\n\n${cap(t)}`, `${cap(t)}.\n\n"${q.f}" (${q.a}) ${e(1)}`, `${e(2)} "${q.f}"\n— ${q.a}`];
    return opts[idx % opts.length];
  }
  const arr = eng.tpl(t, tom, e);
  return arr[idx % arr.length];
}
function makeTags(lang, tema, tom) {
  const eng = ENGINE[lang] || ENGINE.pt;
  const base = (tema || "").toLowerCase().split(/\s+/).filter(w => w.length > 3).slice(0, 3);
  const extra = eng.tags[tom] || [];
  return [...base, ...extra].slice(0, 6).map(x => "#" + x.replace(/[^a-z0-9áéíóúãõâêôç]/gi, "")).filter(x => x.length > 1);
}

